package com.foodplanner.model;

public class Ingredient {

	private String name;
	private UnitOfMeasure unit;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public UnitOfMeasure getUnit() {
		return unit;
	}
	public void setUnit(UnitOfMeasure unit) {
		this.unit = unit;
	} 
}
